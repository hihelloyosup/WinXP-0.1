export function translate(text: string, targetLang: Language): string {
  if (targetLang === 'pt') return text

  const dict = translations[targetLang]
  if (!dict) return text

  // Try exact match first
  if (dict[text]) return dict[text]

  // Try case-insensitive match
  const lowerText = text.toLowerCase()
  for (const [key, value] of Object.entries(dict)) {
    if (key.toLowerCase() === lowerText) {
      return value
    }
  }

  // Try to translate parts of the text
  let translated = text
  for (const [key, value] of Object.entries(dict)) {
    const regex = new RegExp(`\\b${key}\\b`, 'gi')
    translated = translated.replace(regex, value)
  }

  return translated
}

export function translateObject<T extends Record<string, any>>(
  obj: T,
  targetLang: Language,
  keysToTranslate: (keyof T)[]
): T {
  if (targetLang === 'pt') return obj

  const result = { ...obj }
  for (const key of keysToTranslate) {
    if (typeof result[key] === 'string') {
      result[key] = translate(result[key], targetLang) as T[keyof T]
    }
  }
  return result
}
